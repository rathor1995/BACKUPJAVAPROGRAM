package control_statements;

public class switch1 
{
public static void main(String[] args) 
{
	int num=5;
switch (num)
{
case 1:System.out.println("today is mon");  break;
case 2:System.out.println("today is tue");  break;
case 3:System.out.println("today is wed");  break;
case 4:System.out.println("today is thr");  break;
case 5:System.out.println("today is fri");  break;
case 6:System.out.println("today is sat");  break;
case 7:System.out.println("today is sun");  break;
default:System.out.println("enter correct input");break;
}	
}
}
